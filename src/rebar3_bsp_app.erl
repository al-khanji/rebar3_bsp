%%==============================================================================
%% Application Callback Module
%%==============================================================================
-module(rebar3_bsp_app).

%%==============================================================================
%% Behaviours
%%==============================================================================
-behaviour(application).

%%==============================================================================
%% Exports
%%==============================================================================
%% Application Callbacks
-export([ start/2
        , stop/1
        ]).

%%==============================================================================
%% Application Callbacks
%%==============================================================================
-spec start(normal, any()) -> {ok, pid()}.
start(_StartType, _StartArgs) ->
  rebar3_bsp_sup:start_link().

-spec stop(any()) -> ok.
stop(_State) ->
  ok.
